package _26_for_loops;

import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        // Iterator - Comparison - Update
        for (int i = 0; i < 10; i++) {
            System.out.println(" iterator: " + i);
        }

        System.out.println("-----------------------------");

        for (int j = 10; j > 0; j--) {
            System.out.println(" iterator: " + j);
        }
    }
}
