package _07_data_types;

public class Main {
    public static void main(String[] args) {

        // Object type
        String string = new String("This is a string");  // string definition using a constructor
        String name = "Daniel Ouattara"; // string definition using a literal

        // primitives types
        int x = 5;
        System.out.println(x);
        //OR
        Integer y = 5;  // autoboxed Integer variables
        System.out.println(y);

    }
}
